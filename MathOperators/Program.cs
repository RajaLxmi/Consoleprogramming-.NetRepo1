//Arthematic Operators (+,-,*,/,%)
int num1 = 10;
int num2 = 5;
Console.WriteLine("Arthematic Operators");
Console.WriteLine($"Addition : {num1+num2}");
Console.WriteLine($"Subtraction : {num1 - num2}");
Console.WriteLine($"Multiplication : {num1 * num2}");
Console.WriteLine($"Division : {num1 / num2}");
Console.WriteLine($"Modulus : {num1 % num2}");
//Compound Assignment Operators (+=,-=,*=,/=,%=)

Console.WriteLine("Compound Assignment Operators ");
Console.WriteLine($"Addition : {num1 += 4}");
Console.WriteLine($"Subtraction : {num1 -= 4}");
Console.WriteLine($"Multiplication : {num1 *= 4}");
Console.WriteLine($"Division : {num1 /= 4}");
Console.WriteLine($"Modulus : {num1 %= 4}");

